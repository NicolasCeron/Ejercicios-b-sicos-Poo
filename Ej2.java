public class Ej2 {
    
    String personajes;
    boolean tienepelo;

    public Ej2(String personajes, boolean tienepelo) {
        this.personajes = personajes;
        this.tienepelo = tienepelo;
    }

    public String getPersonajes() {
        return personajes;
    }

    public void setPersonajes(String personajes) {
        this.personajes = personajes;
    }

    public boolean isTienepelo() {
        return tienepelo;
    }

    public void setTienepelo(boolean tienepelo) {
        this.tienepelo = tienepelo;
    }
    
    
}
